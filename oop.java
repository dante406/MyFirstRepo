public class oop {
    private int age;
    private double cgpa;
    private String name;


    public void setAge(int age){
        this.age = age;
    }
    public  void setName(String name){
        this.name = name;
    }
    public void setCgpa(double cgpa){
        this.cgpa = cgpa;
    }
    public String getName (){
        return name;
    }
    public int getAge(){
        return age;
    }
    public double getCgpa (){
        return cgpa;
    }
}
